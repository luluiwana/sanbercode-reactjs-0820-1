function luasLingkaran(r){
	return 3.14*r*r;
}
function luasSegitiga(a,t){
	return 0.5*a*t;
}
function luasPersegi(s){
	return s*s;
}

console.log("Luas lingkaran adalah "+luasLingkaran(10)+" cm")
console.log("Luas segitiga adalah "+luasSegitiga(10,10)+" cm")
console.log("Luas Persegi adalah "+luasPersegi(10)+" cm")